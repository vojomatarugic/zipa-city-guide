// Main translation function
export function tr(key: string, language: "sr" | "en"): string {
  // Check if key exists in translations object
  if (key in translations) {
    const typedKey = key as keyof typeof translations;
    // Map 'sr' to 'lat'
    if (language === "sr") {
      return (
        translations[typedKey]?.lat ||
        translations[typedKey]?.en ||
        key
      );
    }
    return (
      translations[typedKey]?.en ||
      translations[typedKey]?.lat ||
      key
    );
  }

  return key;
}